package Modelo;

public class Config {
	
	// direccion
	private String direccion = "";
	private String user = "";
	private String pass = "";
	
	public Config() {
	}

	/**
	 * @return the direccion
	 */
	public String getDireccion() {
		return direccion;
	}

	/**
	 * @return the user
	 */
	public String getUser() {
		return user;
	}

	/**
	 * @return the pass
	 */
	public String getPass() {
		return pass;
	}
	

}